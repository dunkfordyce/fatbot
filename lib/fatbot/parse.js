function parse_raw_irc_command(element) { 
    /*
    This function parses a raw irc command and returns a tuple
    of (prefix, command, args).
    The following is a psuedo BNF of the input text:

    <message>  ::= [':' <prefix> <SPACE> ] <command> <params> <crlf>
    <prefix>   ::= <servername> | <nick> [ '!' <user> ] [ '@' <host> ]
    <command>  ::= <letter> { <letter> } | <number> <number> <number>
    <SPACE>    ::= ' ' { ' ' }
    <params>   ::= <SPACE> [ ':' <trailing> | <middle> <params> ]

    <middle>   ::= <Any *non-empty* sequence of octets not including SPACE
                   or NUL or CR or LF, the first of which may not be ':'>
    <trailing> ::= <Any, possibly *empty*, sequence of octets not including
                     NUL or CR or LF>

    <crlf>     ::= CR LF
    */
    element = element.trim();
    var parts = element.split(' '),
        prefix,
        command,
        args;

    //log('parsing "'+element+'"');
    //log('parts = '+sys.inspect(parts));

    try { 
    if( parts[0][0] == ':' ) {
        prefix = parts[0].substring(1);
        command = parts[1];
        args = parts.slice(2);
    } else {
        prefix = null;
        command = parts[0];
        args = parts.slice(1);
    }

    // convert numeric events 
    if( numeric_events[command] ) { 
        command = numeric_events[command];
    }

    if( args[0][0] == ':' ) {
        args = [args.join(" ").substring(1)];
    } else {
        for( var i=0; i!= args.length; i++ ) {
            //log('looking at part "'+args[i]+'"');
            if( args[i][0] == ':' ) { 
                var newargs = args.slice(0, i);
                newargs.push(args.slice(i).join(" ").substring(1));
                args = newargs;
                break;
            }
        }
    }

    if( prefix ) { 
        prefix = parse_prefix(prefix);
    }
    } catch(e) { 
        log('failed parsing "'+element+'"');
        throw e;
    }

    //log('finally', sys.inspect(prefix), sys.inspect(command), sys.inspect(args));

    return {prefix: prefix, command: command, args: args};
}

function parse_prefix(prefix) {
    var ret = {full: prefix, nick: null, mode: null, user: null, host: null};
    if( prefix.indexOf('!') == -1 ) { 
        ret.nick = prefix;
        return ret;
    }
    var parts = prefix.split('!');

    ret.nick = parts[0];
    if( parts[1].indexOf("=") != -1 ) {
        parts = parts[1].split('=');
        ret.mode = parts[0];
    }
    if( parts[1].indexOf('@') != -1 ) { 
        parts = parts[1].split('@');
        ret.user = parts[0];
        ret.host = parts[1];
    } else {
        ret.user = parts[1];
    }
    return ret;
}

